public class ConditionalOperators{
    public static void main(String[] args){
        // Conditional operators :
        System.out.println(4 == 4);
        System.out.println(4 == 3);
        System.out.println(4 != 4);
        System.out.println(4 != 3);
        System.out.println(4 <= 4);
        System.out.println(4 <= 3);
        System.out.println(4 >= 4);
        System.out.println(4 >= 3);
        System.out.println(4 > 3);
        System.out.println(4 < 3);
    }
}
